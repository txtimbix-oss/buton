export function useSettingsRaw() {
  return useAsyncData('settings-raw', () =>
    $fetch<Record<string, string>>('/api/app-settings').catch(() => ({} as Record<string, string>)),
    { default: () => ({} as Record<string, string>) }
  )
}

export function useSettings() {
  const config = useRuntimeConfig()
  const { data } = useSettingsRaw()
  const settings = computed(() => normalizeSettings({ ...defaultSettings, ...data.value }, config))
  return settings
}
